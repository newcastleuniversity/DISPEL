INVALID_FIXTURE = Flag(
    id_="draw-behavioral-deviation-upmo",
    reason="The phone has not been kept at a portrait upright mode "
    "({percentage}%) for more than 70.0% of the test at the level "
    "{level}.",
    stop_processing=False,
)


