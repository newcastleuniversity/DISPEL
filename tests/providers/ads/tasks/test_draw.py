        ("square_counter_clock-right-02", [INVALID_FIXTURE], 0.0),
        ("square_clock-right", [INVALID_FIXTURE], 65.3),
        ("square_clock-right-02", [INVALID_FIXTURE], 31.4),
        ("infinity-right", [INVALID_FIXTURE], 0.0),
